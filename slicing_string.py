lang="python"

#syntax-[start:stop argument]
print(lang[0:2])
print(lang[2:4])

print(lang[-3:6])

print(lang[:])

print(lang[1:])

print(lang[:3])

print(lang[-6:-1])

#syntax-[start:stop argument:step argument]

print(lang[0:5:1])
print(lang[0:5:2])

#reversing of string 
print(lang[5::-1]) 

print(lang[-1::-1]) 

#reversing of string  by default only providing the step field only
print(lang[::-1])
